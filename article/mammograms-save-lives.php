			<h2>Mammograms Save Lives</h2>

			<figure>

				<img class="full-width" src="/-/media/photos/debbie-with-mammography-technician.jpg" alt="Debbie Bowlby with Mammography Technologist Ginger Conner." />

				<figcaption>Pictured: Debbie Bowlby [L] with Mammography Technologist Ginger Conner [R].</figcaption>

			</figure>

			<p>Annual mammograms save lives. Don&rsquo;t believe it? Just ask Debbie Bowlby, mother of 4, grandmother and great grandmother. What she says about the importance of annual mammograms will change your mind.</p>

			<p>In 2007, Debbie went to the Riverview Women&rsquo;s Center for her annual mammogram just as she had many times before, except this time wasn&rsquo;t routine. That same day, she received a diagnostic mammogram to further view a suspicious area. After further review, it was determined that a biopsy was needed. Then she heard the word &ndash; cancer.</p>

			<p>&ldquo;When the Women&rsquo;s Center called they were so nice and helpful,&rdquo; explained Debbie. &ldquo;They explained the procedures very well, which was comforting because if you don&rsquo;t know what to expect, you almost go into shock.&rdquo;</p>

			<p>On July 10, 2007, Debbie had a lumpectomy at Riverview to remove the suspicious area and some of the surrounding lymph nodes. After surgery, she was referred to the Riverview Women&rsquo;s Boutique for a post-surgery camisole to aid in the healing of the tender area.</p>

			<p>Her journey then led to the Riverview Cancer Center where she received eight weeks of radiation therapy with Dr. Thomas Dugan, a board certified radiation oncologist.</p>

			<p>&ldquo;The staff at the Women&rsquo;s Center treats you like family,&rdquo; explained Debbie. &ldquo;They get to know you by name, and it makes you feel like you are at home and are not just a number.&rdquo;</p>

			<p>Just a year later, the diagnosis of breast cancer came again with the journey going down a very familiar path.This time, however, she was going down the path with friends. &ldquo;They (the Cancer Center staff) know how to make people relax during their treatment,&rdquo; said Debbie. &ldquo;They also know how to make one feel that life is worth living.&rdquo;</p>

			<p>In addition to her wonderful care from the Breast Care Team, Debbie gives credit to what helped her survive, &ldquo;&hellip;my faith, friends, and family &ndash; without them I would have had a hard time but they kept me going.&rdquo;</p>

			<p>For mammogram guidelines, visit <a href="riverview.org">riverview.org</a>.</p>

			<aside>

				<h3>Patient Navigator Featured at Riverview Women&rsquo;s Center</h3>

				<p>Since Debbie Bowlby received her treatment at the Riverview Cancer Center, a patient navigator has been added as a support resource to help those facing breast cancer. The role of the patient navigator, is to assist a patient and her family through the entire treatment process of breast cancer. She is available to further explain the treatment options that the physician has recommended, and becomes the liaison between the patient and physician, providing the necessary resources to the patient. Examples of resources may include referrals to support groups, financial assistance, transportation arrangements and the Women&rsquo;s Boutique for fittings for post-surgical needs.</p>

				<p>For more information about the Women&rsquo;s Center and the patient navigator, call (317) 776-7133.</p>

