			<h2>A Revolution in Speech Therapy</h2>

			<figure>

				<img class="full-width" src="/-/media/photos/corbin-airheart.jpg" alt="Corbin Airheart" />

				<figcaption>

					<p>Pictured: Corbin Airheart using an iPad in his therapy session.</p>

				</figcaption>

			</figure>

			<p>It&rsquo;s hard to go anywhere these days without seeing people working &ndash; or playing &ndash; on their smartphones, iPads, netbooks, and other forms of technology we just can&rsquo;t imagine living without. One of the many benefits these devices offer is access to thousands of applications in hundreds of categories.</p>

			<p>One Riverview Hospital speech-language pathologist took notice of the unlimited possibilities offered through educational iPad applications geared specifically for speech therapy clients, and she is certainly glad she did!</p>

			<p>In early 2010, Riverview Hospital speech-language pathologist Cheryl Stitt learned about new applications developed specifically for speech therapy purposes. Using her personal iPhone first, then shortly thereafter her personal iPad, Cheryl installed a few applications she had learned about through her network of speech-language pathologists. She piloted the applications with some of her clients in conjunction with standard therapy and the results were outstanding &ndash; her clients really seemed to thrive and responded well to the technology. As an added benefit, many of the applications offered built-in data tracking, making it easy to track client progress over time.</p>

			<figure>

				<img class="full-width" src="/-/media/photos/corbin-airheart-with-cheryl-stitt.jpg" alt="Speech-Language Pathologist Cheryl Stitt and patient Corbin Airheart, use the iPad in his therapy session." />

				<figcaption>

					<p>Pictured: Speech-Language Pathologist Cheryl Stitt and patient Corbin Airheart, use the iPad in his therapy session.</p>

				</figcaption>

			</figure>

			<p>Throughout the year, Cheryl continued to add more applications to her iPad and witnessed the impact the use of the applications were having on her clients. She knew the iPad had to become a standard. In early 2011, through grants from Disney Worldwide Outreach and the Riverview Hospital Women&rsquo;s and Children&rsquo;s Unit, the speech therapy department was able to purchase three iPads for use in speech therapy. &ldquo;The use of iPads with our speech therapy clients has truly revolutionized the way we are able to conduct our therapy sessions and interact with our clients,&rdquo; Cheryl says. &ldquo;It has made some previous methods of therapy somewhat obsolete.&rdquo;</p>

			<p>For one of Cheryl&rsquo;s pediatric clients, Corbin Airheart, who has been diagnosed with mild Autism, the iPad has become a staple in his therapy sessions. Guided by Cheryl, Corbin enjoys and benefits from the interactive applications (particularly play-based learning) that teach cause and effect and comprehension. Various applications are also used during Corbin&rsquo;s therapy sessions to encourage his participation and to help hold his attention. He frequently asks for the same &ldquo;games&rdquo; when he comes to therapy &ndash; and those are used as a reward. Cheryl loves that she is able to download educational applications that cater to his specific interests &ndash; currently trains &ndash; with a few simple clicks.</p>

			<p>&ldquo;Corbin has made significant strides in a short amount of time,&rdquo; Cheryl says. &ldquo;I think the iPad was made for him!&rdquo; The use of the iPad as a reward tool has become very popular among not only speech therapy clients but also for physical therapy clients.&ldquo;If clients know they will be able to play their favorite game or listen to their favorite music, they are more motivated to participate and stay focused throughout their session,&rdquo; said Cheryl.</p>

			<p>Cheryl estimates that the iPad is used for about a third of her therapy sessions, which typically run about 45 minutes, and she foresees its use increasing in the future, as she and other therapists continue to learn more about available applications. &ldquo;The opportunities are endless,&rdquo; Cheryl says. As an added benefit, Cheryl is able to give &ldquo;homework&rdquo; to her clients who have iPads at home, which aids in their momentum.</p>

			<p>For adult clients, particularly those who have suffered a stroke, those living with Multiple Sclerosis or Parkinson&rsquo;s disease, or for those with developmental delays, the iPad has been equally as effective.Various applications assist with memory, cognitive learning, articulation, motor skills, and much more.And clients love it &ndash; even those who were a bit hesitant at first to try something new.</p>

			<p>&ldquo;We are so happy to see our clients of all ages really grasping new technology and we love their enthusiasm, Cheryl says. &ldquo;Years ago, we never would have dreamed technology like this would be possible.&rdquo;</p>

